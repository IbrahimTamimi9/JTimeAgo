package io.inspace.jtimeago.languages.lang;

/**
 * Abstract class base for all language classes
 * It's also default language - english;
 */
public class AbstractLanguage {

  protected String ago = "ago";
  protected String minute = "a minute";
  protected String minutes = "minutes";
  protected String seconds = "seconds";
  protected String second = "a second";
  protected String hour = "a hour";
  protected String hours = "hours";
  protected String justNow = "Just now";
  protected String week = "a week";
  protected String lastWeek = "Last week";
  protected String weeks = "weeks";
  protected String month = "a month";
  protected String lastMonth = "Last month";
  protected String months = "months";
  protected String year = "a year";
  protected String lastYear = "Last year";
  protected String years = "years";
  protected String yesterday = "Yesterday";
  protected String day = "a day";
  protected String days = "days";

  public String getAgo() {
    return ago;
  }

  public void setAgo(String ago) {
    this.ago = ago;
  }

  public String getMinute() {
    return minute;
  }

  public void setMinute(String minute) {
    this.minute = minute;
  }

  public String getMinutes() {
    return minutes;
  }

  public void setMinutes(String minutes) {
    this.minutes = minutes;
  }

  public String getSeconds() {
    return seconds;
  }

  public void setSeconds(String seconds) {
    this.seconds = seconds;
  }

  public String getSecond() {
    return second;
  }

  public void setSecond(String second) {
    this.second = second;
  }

  public String getHour() {
    return hour;
  }

  public void setHour(String hour) {
    this.hour = hour;
  }

  public String getHours() {
    return hours;
  }

  public void setHours(String hours) {
    this.hours = hours;
  }

  public String getJustNow() {
    return justNow;
  }

  public void setJustNow(String justNow) {
    this.justNow = justNow;
  }

  public String getWeek() {
    return week;
  }

  public void setWeek(String week) {
    this.week = week;
  }

  public String getLastWeek() {
    return lastWeek;
  }

  public void setLastWeek(String lastWeek) {
    this.lastWeek = lastWeek;
  }

  public String getWeeks() {
    return weeks;
  }

  public void setWeeks(String weeks) {
    this.weeks = weeks;
  }

  public String getMonth() {
    return month;
  }

  public void setMonth(String month) {
    this.month = month;
  }

  public String getLastMonth() {
    return lastMonth;
  }

  public void setLastMonth(String lastMonth) {
    this.lastMonth = lastMonth;
  }

  public String getMonths() {
    return months;
  }

  public void setMonths(String months) {
    this.months = months;
  }

  public String getYear() {
    return year;
  }

  public void setYear(String year) {
    this.year = year;
  }

  public String getLastYear() {
    return lastYear;
  }

  public void setLastYear(String lastYear) {
    this.lastYear = lastYear;
  }

  public String getYears() {
    return years;
  }

  public void setYears(String years) {
    this.years = years;
  }

  public String getYesterday() {
    return yesterday;
  }

  public void setYesterday(String yesterday) {
    this.yesterday = yesterday;
  }

  public String getDay() {
    return day;
  }

  public void setDay(String day) {
    this.day = day;
  }

  public String getDays() {
    return days;
  }

  public void setDays(String days) {
    this.days = days;
  }
}
